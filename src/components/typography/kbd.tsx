import React from "react";

import { cn } from "@/lib/utils";

function Kbd({ className, ...props }: React.ComponentProps<"kbd">) {
  return (
    <kbd
      data-slot="kbd"
      className={cn(
        `
          pointer-events-none inline-flex h-5 w-fit min-w-5 items-center
          justify-center gap-1 rounded-sm bg-foreground px-1 font-sans text-xs
          font-medium text-background select-none
        `,
        "border-b-2 border-muted-foreground",
        "[&_svg:not([class*='size-'])]:size-3",
        `
          in-data-[slot=tooltip-content]:bg-background/20
          in-data-[slot=tooltip-content]:text-background
          dark:in-data-[slot=tooltip-content]:bg-background/10
        `,
        className
      )}
      {...props}
    />
  );
}

function KbdGroup({
  className,
  keys,
  withPlusSigns,
  ...props
}: React.ComponentProps<"div"> & {
  keys?: string[];
  withPlusSigns?: boolean;
}) {
  return (
    <kbd
      className={cn("inline-flex items-center gap-1", className)}
      data-slot="kbd-group"
      {...props}
    >
      {keys?.map((key, index) => (
        <React.Fragment key={index}>
          <Kbd>{key}</Kbd>
          {withPlusSigns && index < keys.length - 1 && <span>+</span>}
        </React.Fragment>
      ))}
    </kbd>
  );
}

export { Kbd, KbdGroup };
